    private void jLabelToolBarSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelToolBarSaveMouseClicked
        try {
            if (!jTextFieldName.getText().equals("") && 
                    !jFormattedTextFieldDeadline.getText().isEmpty()){
                Task task = new Task();
                task.setIdProject(project.getId());
                task.setName(jTextFieldName.getText());
                task.setDescription(jTextAreaDescription.getText());
                task.setNotes(jTextAreaNotes.getText());
                task.setIsCompleted(false);

                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                Date deadline;
                deadline = dateFormat.parse(jFormattedTextFieldDeadline.getText());
                task.setDeadline(deadline);

                controller.save(task);
                JOptionPane.showMessageDialog(rootPane, "Oba, Voce criou uma nova tarefa!");

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "VocÃª deve inserir um nome e uma data!");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Houve um erro ao criar a tarefa\n!"+e);
        }
        
    }//GEN-LAST:event_jLabelToolBarSaveMouseClicked

